package com.aysekarapinar.ibb_ecodation_javafx.tutorials.javacore._1_week;

public class _03_JavaPrimitiveTypes {
    public static void main(String[] args) {

        // Primitive Types: 8 tanedir. Null değer alamaz.
        //Tam Sayılar (4)
        byte b1=127;
        System.out.println("byte: "+b1);
        short s1=32767;
        System.out.println("short: "+s1);
        int i1=21456789;
        System.out.println("int: "+i1);
        long l1=123456789034L;
        System.out.println("Long: "+l1);

        //Virgüllü (Floating Point)
        float f2=3.14f;
        System.out.println("Float: "+f2);
        double d2=3.14d;
        System.out.println("Double: "+d2);

        // Boolean
        boolean b3=true;
        System.out.println("Boolean: "+b3);

        //Char
        char c4='a';
        System.out.println("Char: "+c4);
    }
}
