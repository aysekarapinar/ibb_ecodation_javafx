package com.aysekarapinar.ibb_ecodation_javafx.tutorials.javacore._1_week;

public class _06_Math {
    public static void main(String[] args) {
        //Nan: Not A Number
        System.out.println("En küçük: "+Math.min(-300,10));
        System.out.println("En büyük: "+Math.max(-300,10));
        System.out.println("En büyük: "+Math.abs(-7));
        System.out.println("Karekök: "+Math.sqrt(Math.abs(-25)));
        System.out.println("Üslü: "+Math.pow(2,5));
        System.out.println("Alta yuvarla: "+Math.floor(2.9));
        System.out.println("Üste yuvarla: "+Math.ceil(2.00001));
        System.out.println("Ortalama yuvarla: "+Math.round(3.4));
        System.out.println("Ortalama yuvarla: "+Math.round(3.5)); // x>=4 ve aşağı yuvarla
        System.out.println("Triginometrik Sinüs: "+Math.sin(1)); // x>=4 ve aşağı yuvarla
        System.out.println("Triginometrik Sinüs: "+Math.asin(1)); // x>=4 ve aşağı yuvarla
        System.out.println("Triginometrik Sinüs: "+Math.cos(1)); // x>=4 ve aşağı yuvarla
        System.out.println("Triginometrik Sinüs: "+Math.tan(1));
        System.out.println("PI Sayısı: "+Math.PI);// x>=4 ve aşağı yuvarla
        System.out.println("E Sayısı: "+Math.E);// x>=4 ve aşağı yuvarla


    }
}
